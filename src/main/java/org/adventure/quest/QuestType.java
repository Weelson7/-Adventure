package org.adventure.quest;

/**
 * Represents the type of quest objective.
 */
public enum QuestType {
    /** Quest to explore a location */
    EXPLORE,
    
    /** Quest to retrieve an item or artifact */
    RETRIEVE,
    
    /** Quest to defeat enemies or creatures */
    DEFEAT,
    
    /** Quest to investigate a mystery or phenomenon */
    INVESTIGATE,
    
    /** Quest to escort or protect an NPC */
    ESCORT,
    
    /** Quest to deliver items or messages */
    DELIVER,
    
    /** Quest to craft or create something */
    CRAFT
}
