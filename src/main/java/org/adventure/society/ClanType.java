package org.adventure.society;

/**
 * Enum representing types of social organizations.
 * 
 * Types:
 * - CLAN: Basic social group, small to medium size
 * - KINGDOM: Large hierarchical organization with multiple clans
 * - GUILD: Professional or trade-based organization
 * - AGRICULTURAL: Farming-focused clan
 * - MERCANTILE: Trading-focused clan
 * - MILITARY: Warrior-focused clan
 * - MINING: Resource extraction focused clan
 * - NOMADIC: Mobile/wandering clan
 */
public enum ClanType {
    CLAN,
    KINGDOM,
    GUILD,
    AGRICULTURAL,
    MERCANTILE,
    MILITARY,
    MINING,
    NOMADIC
}
