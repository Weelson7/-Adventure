package org.adventure.prophecy;

/**
 * Represents the type of prophecy.
 */
public enum ProphecyType {
    /** Prophecy predicting disaster or destruction */
    DOOM,
    
    /** Prophecy predicting salvation or rescue */
    SALVATION,
    
    /** Prophecy predicting transformation of land/people */
    TRANSFORMATION,
    
    /** Prophecy predicting awakening of ancient power */
    AWAKENING,
    
    /** Prophecy predicting war between factions */
    WAR,
    
    /** Prophecy predicting peace and prosperity */
    PROSPERITY
}
